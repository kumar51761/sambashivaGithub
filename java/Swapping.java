package com.learningjava.java;

public class Swapping 
{
    public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		int c=0;
    	System.out.println(a);
    	System.out.println(b);
    	System.out.println("************");
    	c=a;
    	a=b;
    	b=c;
    	System.out.println(c);
    	System.out.println("************");
    	System.out.println(a);
    	System.out.println(b);
		
	}

}
